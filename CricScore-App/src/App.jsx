// ─── TeamsScreen ─────────────────────────────────────────────────────────────
function TeamsScreen({ dark, match, setMatch, setScreen }) {
  const [newPlayerA, setNewPlayerA] = useState("");
  const [newPlayerB, setNewPlayerB] = useState("");
  const [playersA, setPlayersA] = useState(match?.playersA || []);
  const [playersB, setPlayersB] = useState(match?.playersB || []);

  const card = dark ? "bg-[#0d1929] border-[#1e3a5f]" : "bg-white border-slate-200";
  const inp = `flex-1 px-4 py-3 rounded-xl border ${dark ? "bg-[#111c2d] border-[#1e3a5f] text-white placeholder-slate-500" : "bg-slate-50 border-slate-300 text-slate-900"} text-base focus:outline-none focus:border-orange-500`;

  const addA = () => { if (newPlayerA.trim() && playersA.length < 11) { setPlayersA(p => [...p, newPlayerA.trim()]); setNewPlayerA(""); } };
  const addB = () => { if (newPlayerB.trim() && playersB.length < 11) { setPlayersB(p => [...p, newPlayerB.trim()]); setNewPlayerB(""); } };

  const proceed = () => {
    if (playersA.length < 2 || playersB.length < 2) { alert("Add at least 2 players per team."); return; }
    const updated = { ...match, playersA, playersB };
    setMatch(updated);
    setScreen(SCREENS.TOSS);
  };

  const TeamSection = ({ label, players, setPlayers, newVal, setNew, onAdd, color }) => (
    <div className={`rounded-2xl border p-4 ${card}`}>
      <h3 className={`font-black text-lg mb-3 ${color}`}>{label} <span className={`text-sm ${dark ? "text-slate-400" : "text-slate-500"}`}>({players.length}/11)</span></h3>
      <div className="flex gap-2 mb-3">
        <input className={inp} placeholder="Player name" value={newVal} onChange={e => setNew(e.target.value)} onKeyDown={e => e.key === "Enter" && onAdd()} />
        <button onClick={onAdd} className={`px-4 py-3 rounded-xl font-bold bg-orange-500 text-black`}>+</button>
      </div>
      <div className="space-y-2 max-h-48 overflow-y-auto">
        {players.map((p, i) => (
          <div key={i} className={`flex justify-between items-center px-3 py-2 rounded-xl ${dark ? "bg-[#111c2d]" : "bg-slate-50"}`}>
            <span className="text-sm font-semibold">{i + 1}. {p}</span>
            <button onClick={() => setPlayers(pl => pl.filter((_, j) => j !== i))} className="text-red-400 text-xs hover:text-red-300">✕</button>
          </div>
        ))}
      </div>
    </div>
  );

  return (
    <div className="min-h-screen px-4 py-8">
      <div className="w-full max-w-md mx-auto">
        <button onClick={() => setScreen(SCREENS.SETUP)} className="text-orange-400 text-sm mb-4 flex items-center gap-1">← Back</button>
        <h2 className="text-2xl font-black mb-6">👥 Add Players</h2>
        <div className="space-y-4">
          <TeamSection label={match.teamA} players={playersA} setPlayers={setPlayersA} newVal={newPlayerA} setNew={setNewPlayerA} onAdd={addA} color="text-orange-400" />
          <TeamSection label={match.teamB} players={playersB} setPlayers={setPlayersB} newVal={newPlayerB} setNew={setNewPlayerB} onAdd={addB} color="text-cyan-400" />
        </div>
        <button onClick={proceed} className="w-full mt-6 py-4 bg-orange-500 hover:bg-orange-400 text-black font-black text-lg rounded-2xl transition-all active:scale-95">
          Next: Toss →
        </button>
      </div>
    </div>
  );
}

// ─── TossScreen ───────────────────────────────────────────────────────────────
function TossScreen({ dark, match, setMatch, setScreen }) {
  const [tossWinner, setTossWinner] = useState(null);
  const [choice, setChoice] = useState(null);
  const card = dark ? "bg-[#0d1929] border-[#1e3a5f]" : "bg-white border-slate-200";

  const startMatch = () => {
    if (!tossWinner || !choice) { alert("Complete toss details."); return; }
    const batting1st = choice === "bat" ? tossWinner : (tossWinner === match.teamA ? match.teamB : match.teamA);
    const bowling1st = batting1st === match.teamA ? match.teamB : match.teamA;
    const tossStr = `${tossWinner} won toss, elected to ${choice}`;
    const batters = batting1st === match.teamA ? match.playersA : match.playersB;
    const bowlers2 = bowling1st === match.teamA ? match.playersA : match.playersB;
    const initBatsmen = batters.map(n => ({ name: n, runs: 0, balls: 0, fours: 0, sixes: 0, out: false, howOut: null }));
    const initBowlers = bowlers2.map(n => ({ name: n, overs: 0, balls: 0, runs: 0, wickets: 0, maidens: 0 }));
    const innings1 = {
      battingTeam: batting1st, bowlingTeam: bowling1st,
      runs: 0, wickets: 0, balls: 0, extras: { wides: 0, noballs: 0, byes: 0, total: 0 },
      batsmen: initBatsmen, bowlers: initBowlers,
      currentBat1: 0, currentBat2: 1, currentBowler: 0,
      ballLog: [], completed: false,
    };
    const updated = { ...match, toss: tossStr, innings1, state: "innings1" };
    setMatch(updated);
    setScreen(SCREENS.SCORING);
  };

  const Btn = ({ label, val, active, setActive }) => (
    <button onClick={() => setActive(val)} className={`flex-1 py-3 rounded-xl font-bold transition-all text-sm ${active === val ? "bg-orange-500 text-black" : dark ? "bg-[#111c2d] border border-[#1e3a5f] text-slate-300" : "bg-slate-100 border border-slate-300 text-slate-700"}`}>{label}</button>
  );

  return (
    <div className="min-h-screen flex flex-col items-center justify-center px-4">
      <div className={`w-full max-w-md rounded-2xl border p-6 shadow-2xl ${card}`}>
        <button onClick={() => setScreen(SCREENS.TEAMS)} className="text-orange-400 text-sm mb-4">← Back</button>
        <div className="text-center mb-6">
          <div className="text-5xl mb-2">🪙</div>
          <h2 className="text-2xl font-black">Toss</h2>
        </div>
        <div className="space-y-5">
          <div>
            <label className={`text-xs font-bold mb-2 block ${dark ? "text-slate-400" : "text-slate-500"}`}>WHO WON THE TOSS?</label>
            <div className="flex gap-2">
              <Btn label={match.teamA} val={match.teamA} active={tossWinner} setActive={setTossWinner} />
              <Btn label={match.teamB} val={match.teamB} active={tossWinner} setActive={setTossWinner} />
            </div>
          </div>
          {tossWinner && (
            <div>
              <label className={`text-xs font-bold mb-2 block ${dark ? "text-slate-400" : "text-slate-500"}`}>ELECTED TO?</label>
              <div className="flex gap-2">
                <Btn label="🏏 Bat" val="bat" active={choice} setActive={setChoice} />
                <Btn label="🎯 Bowl" val="bowl" active={choice} setActive={setChoice} />
              </div>
            </div>
          )}
        </div>
        {tossWinner && choice && (
          <div className={`mt-4 p-3 rounded-xl text-center text-sm font-semibold ${dark ? "bg-[#0a1628]" : "bg-orange-50"}`}>
            <span className="text-orange-400">{choice === "bat" ? tossWinner : (tossWinner === match.teamA ? match.teamB : match.teamA)}</span> will bat first
          </div>
        )}
        <button onClick={startMatch} className="w-full mt-6 py-4 bg-orange-500 hover:bg-orange-400 text-black font-black text-lg rounded-2xl transition-all active:scale-95">
          🏏 Start Match
        </button>
      </div>
    </div>
  );
}

// ─── ScoringScreen ────────────────────────────────────────────────────────────
