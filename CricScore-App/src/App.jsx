import { useState, useEffect, useCallback } from "react";

// ─── Utility ────────────────────────────────────────────────────────────────
const LS_KEY = "cricket_app_v1";
const save = (data) => localStorage.setItem(LS_KEY, JSON.stringify(data));
const load = () => { try { return JSON.parse(localStorage.getItem(LS_KEY)) || {}; } catch { return {}; } };

const rr = (runs, balls) => balls === 0 ? "0.00" : ((runs / balls) * 6).toFixed(2);
const overStr = (balls) => `${Math.floor(balls / 6)}.${balls % 6}`;
const nowDate = () => new Date().toLocaleDateString("en-IN", { day: "2-digit", month: "short", year: "numeric" });

// ─── PDF Generator ───────────────────────────────────────────────────────────
function generatePDF(match) {
  const { teamA, teamB, innings1, innings2, venue, motm, date, toss, overs } = match;
  const winner = (() => {
    if (!innings2 || !innings2.completed) return null;
    if (innings2.runs > innings1.runs) return `${teamB} won by ${innings2.wickets < 10 ? 10 - innings2.wickets : 0} wickets`;
    if (innings1.runs > innings2.runs) return `${teamA} won by ${innings1.runs - innings2.runs} runs`;
    return "Match Tied";
  })();

  const batTable = (inn, team) => {
    const rows = (inn.batsmen || []).map(b =>
      `<tr><td>${b.name}</td><td>${b.howOut || "not out"}</td><td>${b.runs}</td><td>${b.balls}</td><td>${b.fours}</td><td>${b.sixes}</td><td>${b.balls > 0 ? ((b.runs / b.balls) * 100).toFixed(1) : "0.0"}</td></tr>`
    ).join("");
    return `<h3 style="color:#f97316;margin:12px 0 4px">${team} Batting</h3>
    <table style="width:100%;border-collapse:collapse;font-size:11px">
      <thead><tr style="background:#1e293b;color:#94a3b8">${["Batsman","Dismissal","R","B","4s","6s","SR"].map(h=>`<th style="padding:4px 6px;text-align:left">${h}</th>`).join("")}</tr></thead>
      <tbody>${rows}</tbody>
    </table>
    <p style="font-size:11px;margin:4px 0">Extras: ${inn.extras?.total || 0} (wd: ${inn.extras?.wides||0}, nb: ${inn.extras?.noballs||0})</p>
    <p style="font-size:13px;font-weight:bold;margin:4px 0">Total: ${inn.runs}/${inn.wickets} (${overStr(inn.balls)} ov)</p>`;
  };

  const bowlTable = (inn) => {
    const rows = (inn.bowlers || []).filter(b => b.balls > 0).map(b =>
      `<tr><td>${b.name}</td><td>${overStr(b.balls)}</td><td>${b.runs}</td><td>${b.wickets}</td><td>${b.balls > 0 ? ((b.runs / b.balls) * 6).toFixed(2) : "0.00"}</td></tr>`
    ).join("");
    return `<h3 style="color:#06b6d4;margin:12px 0 4px">Bowling</h3>
    <table style="width:100%;border-collapse:collapse;font-size:11px">
      <thead><tr style="background:#1e293b;color:#94a3b8">${["Bowler","O","R","W","Econ"].map(h=>`<th style="padding:4px 6px;text-align:left">${h}</th>`).join("")}</tr></thead>
      <tbody>${rows}</tbody>
    </table>`;
  };

  const html = `<!DOCTYPE html><html><head><meta charset="UTF-8">
  <title>Scorecard - ${teamA} vs ${teamB}</title>
  <style>
    body{font-family:Georgia,serif;background:#0f172a;color:#e2e8f0;padding:24px;max-width:700px;margin:auto}
    table{width:100%;border-collapse:collapse} td,th{padding:4px 6px;border-bottom:1px solid #1e293b}
    tr:hover{background:#1e293b} h1{color:#f97316} h2{color:#94a3b8;border-bottom:1px solid #334155;padding-bottom:4px}
    .badge{display:inline-block;background:#f97316;color:#000;padding:2px 8px;border-radius:4px;font-size:12px;font-weight:bold}
  </style></head><body>
  <h1>🏏 Cricket Scorecard</h1>
  <p><strong>Match:</strong> ${teamA} vs ${teamB} | <strong>Date:</strong> ${date || nowDate()} | <strong>Venue:</strong> ${venue || "Local Ground"}</p>
  <p><strong>Format:</strong> T${overs || 20} | <strong>Toss:</strong> ${toss || "—"}</p>
  ${winner ? `<p class="badge">${winner}</p>` : ""}
  ${motm ? `<p><strong>🏆 Man of the Match:</strong> ${motm}</p>` : ""}
  <h2>1st Innings — ${teamA}</h2>
  ${innings1 ? batTable(innings1, teamA) : "<p>No data</p>"}
  ${innings1 ? bowlTable(innings1) : ""}
  <h2>2nd Innings — ${teamB}</h2>
  ${innings2 ? batTable(innings2, teamB) : "<p>Not played</p>"}
  ${innings2 ? bowlTable(innings2) : ""}
  <p style="font-size:10px;color:#475569;margin-top:24px">Generated by Cricket Scorer App</p>
  </body></html>`;

  const w = window.open("", "_blank");
  w.document.write(html);
  w.document.close();
  setTimeout(() => w.print(), 400);
}

// ─── Screens ─────────────────────────────────────────────────────────────────
const SCREENS = { HOME: "home", SETUP: "setup", TOSS: "toss", TEAMS: "teams", SCORING: "scoring", INNINGS_BREAK: "innings_break", SUMMARY: "summary", HISTORY: "history" };

// ════════════════════════════════════════════════════════════════════════════
export default function App() {
  const [dark, setDark] = useState(true);
  const [screen, setScreen] = useState(SCREENS.HOME);
  const [match, setMatch] = useState(null);
  const [history, setHistory] = useState([]);
  const [toast, setToast] = useState(null);

  useEffect(() => {
    const d = load();
    if (d.history) setHistory(d.history);
    if (d.activeMatch) { setMatch(d.activeMatch); setScreen(SCREENS.SCORING); }
  }, []);

  const showToast = (msg, type = "success") => {
    setToast({ msg, type });
    setTimeout(() => setToast(null), 2500);
  };

  const saveMatch = useCallback((m) => {
    setMatch(m);
    const d = load();
    d.activeMatch = m;
    save(d);
  }, []);

  const finishMatch = useCallback((m) => {
    const d = load();
    const finished = { ...m, finishedAt: Date.now() };
    d.activeMatch = null;
    d.history = [finished, ...(d.history || []).slice(0, 19)];
    save(d);
    setHistory(d.history);
    setMatch(finished);
    setScreen(SCREENS.SUMMARY);
  }, []);

  const bg = dark ? "bg-[#080f1a]" : "bg-slate-100";
  const text = dark ? "text-slate-100" : "text-slate-900";

  return (
    <div className={`min-h-screen ${bg} ${text} transition-colors duration-300`} style={{ fontFamily: "'Segoe UI', system-ui, sans-serif" }}>
      {/* Toast */}
      {toast && (
        <div className={`fixed top-4 left-1/2 -translate-x-1/2 z-50 px-5 py-3 rounded-xl text-white font-semibold shadow-2xl transition-all duration-300 ${toast.type === "error" ? "bg-red-600" : "bg-emerald-600"}`}>
          {toast.msg}
        </div>
      )}

      {screen === SCREENS.HOME && <HomeScreen dark={dark} setDark={setDark} setScreen={setScreen} history={history} setMatch={setMatch} showToast={showToast} />}
      {screen === SCREENS.SETUP && <SetupScreen dark={dark} setScreen={setScreen} setMatch={saveMatch} showToast={showToast} />}
      {screen === SCREENS.TEAMS && <TeamsScreen dark={dark} match={match} setMatch={saveMatch} setScreen={setScreen} showToast={showToast} />}
      {screen === SCREENS.TOSS && <TossScreen dark={dark} match={match} setMatch={saveMatch} setScreen={setScreen} />}
      {screen === SCREENS.SCORING && match && <ScoringScreen dark={dark} match={match} setMatch={saveMatch} setScreen={setScreen} finishMatch={finishMatch} showToast={showToast} />}
      {screen === SCREENS.INNINGS_BREAK && match && <InningsBreak dark={dark} match={match} setMatch={saveMatch} setScreen={setScreen} showToast={showToast} />}
      {screen === SCREENS.SUMMARY && match && <SummaryScreen dark={dark} match={match} setScreen={setScreen} showToast={showToast} />}
      {screen === SCREENS.HISTORY && <HistoryScreen dark={dark} history={history} setScreen={setScreen} setMatch={setMatch} />}
    </div>
  );
}

